/**
 * Get models by tier
 */
export function getModelsByTier(tier: 'budget' | 'standard' | 'premium' | 'ultra'): ImageModel[] {
  return Object.values(IMAGE_MODELS).filter(model => model.tier === tier);
}

/**
 * Get models by capability
 */
export function getModelsByCapability(capability: 'textToImage' | 'imageToImage' | 'editing' | 'lora'): ImageModel[] {
  return Object.values(IMAGE_MODELS).filter(model => model.capabilities[capability]);
}

/**
 * Get model by ID
 */
export function getModelById(id: string): ImageModel | undefined {
  return IMAGE_MODELS[id];
}

/**
 * Get all models
 */
export function getAllModels(): ImageModel[] {
  return Object.values(IMAGE_MODELS);
}

/**
 * Get models by provider
 */
export function getModelsByProvider(provider: string): ImageModel[] {
  return Object.values(IMAGE_MODELS).filter(model => model.provider === provider);
}
